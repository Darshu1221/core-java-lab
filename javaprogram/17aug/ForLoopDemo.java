class ForLoopDemo
{
	static void printNumbers()
	{
		System.out.println("Printing numbers");
		for(int count=0;count<=10;count++)
		{
			System.out.println(count);
		}
		
	}
	public static void main(String args[])
	{
		printNumbers();
	}
}
